public class BitwiseNOT {
    public static void main(String[] args) {
        System.out.println(~5);   // ~0101 = 1010 (-6)
        System.out.println(~10);  // ~1010 = 0101 (-11)
        System.out.println(~7);   // ~0111 = 1000 (-8)
        System.out.println(~0);   // ~0000 = 1111 (-1)
        System.out.println(~1);   // ~0001 = 1110 (-2)
    }
}

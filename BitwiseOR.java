public class BitwiseOR {
    public static void main(String[] args) {
        System.out.println(5 | 3);  // 0101 | 0011 = 0111 (7)
        System.out.println(10 | 7); // 1010 | 0111 = 1111 (15)
        System.out.println(12 | 8); // 1100 | 1000 = 1100 (12)
        System.out.println(6 | 4);  // 0110 | 0100 = 0110 (6)
        System.out.println(15 | 9); // 1111 | 1001 = 1111 (15)
    }
}

public class RightShift {
    public static void main(String[] args) {
        System.out.println(5 >> 1);  // 0101 >> 1 = 0010 (2)
        System.out.println(10 >> 2); // 1010 >> 2 = 0010 (2)
        System.out.println(15 >> 3); // 1111 >> 3 = 0001 (1)
        System.out.println(8 >> 1);  // 1000 >> 1 = 0100 (4)
        System.out.println(20 >> 2); // 10100 >> 2 = 0101 (5)
    }
}


public class BitwiseAND {
    public static void main(String[] args) {
        System.out.println(5 & 3);  // 0101 & 0011 = 0001 (1)
        System.out.println(10 & 7); // 1010 & 0111 = 0010 (2)
        System.out.println(12 & 8); // 1100 & 1000 = 1000 (8)
        System.out.println(6 & 4);  // 0110 & 0100 = 0100 (4)
        System.out.println(15 & 9); // 1111 & 1001 = 1001 (9)
    }
}
